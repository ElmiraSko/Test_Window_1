public class Questions {

    private int id;
    private String quest;

    public Questions(int id, String quest){
    this.id = id;
    this.quest = quest;
}

    public int getId() {
        return id;
    }
    public void setId(int id){this.id = id;}

    public String getQuest() {
        return quest;
    }
    public void  setQuest(String quest){this.quest = quest;}
    }
